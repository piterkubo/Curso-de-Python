#Lista 

lista = ['Ana', 'Lia', 'Rui','Paulo', 'Dani']


#Percorrendo a lista pelo range
print(lista[1:3])
print(lista[1:-1])
print(lista[1:])
print(lista[:-1])
print(lista[:])
print(lista[::2])
print(lista[::-1])


# Deletando um elemento de uma lista
del lista[2]
print(lista)

del lista[1:]

print(lista)