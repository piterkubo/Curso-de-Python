# Operadores de atribuição

'''
= Atribuição

+= Incrementando valor

-= Descremento valor

=* Atribuição multiplicativa

/= Atribuição divisivel

%= Atribuição modular

**= Atribuição expontencial

//= Atribuicao resto da divisao



'''

a = 3

a = a + 7
print(a)

a += 5
print(a)


a -= 3
print(a)

a *= 2
print(a)

a /= 4
print(a)

a %= 4
print(a)

a **= 8
print(a)

a //= 256
print(a)

